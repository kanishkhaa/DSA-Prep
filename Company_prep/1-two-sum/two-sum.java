class Solution {
    public int[] twoSum(int[] nums, int target) {
        HashMap<Integer,Integer> map = new HashMap<>();
        for(int i=0;i<nums.length;i++) {
            int num = nums[i];
            int com = target - nums[i];
            if(map.containsKey(com)) {
                return new int[]{map.get(com), i};
            }
           map.put(num,i);
        }
        return new int[]{-1,-1};
    }
}